// Chart data helpers
export const getDepartmentSavingsData = () => {
  return departments.map((dept) => ({
    name: dept.name,
    savings: dept.costImpactPerYear,
    shortName: dept.name.split(' ')[0],
  }));
};

export const getAutomationDistributionData = () => {
  const categoryCount: Record<string, number> = {};
  automationOpportunities.forEach((opp) => {
    categoryCount[opp.category] = (categoryCount[opp.category] || 0) + 1;
  });

  const categoryLabels: Record<string, string> = {
    process: 'Process',
    communication: 'Communication',
    data: 'Data & Analytics',
    reporting: 'Reporting',
    integration: 'Integration',
  };

  return Object.entries(categoryCount).map(([key, value]) => ({
    name: categoryLabels[key] || key,
    value,
    category: key,
  }));
};
